package round;

/** RoundPeg class. */
public class RoundPeg {
    private final double radius;

    /**
     * Constructor.
     * @param radius The radius of the peg.
     */
    public RoundPeg(double radius) {
        this.radius = radius;
    }

    /** Return the radius of the peg. */
    public double getRadius() {
        return radius;
    }
}
